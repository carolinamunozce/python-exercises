"""
Pedir dos números al usuario y hacer todas las operaciones básicas
de una calculadora y mostrarlo por pantalla 
"""

numero1 = int(input(" Ingrese el primer número: "))
numero2 = int(input(" Ingrese del segundo número: "))
print('*****Calculadora*****')
print(f'La suma de los dos números es:  {numero1+numero2}')
print(f'La resta de los dos números es:  {numero1 - numero2}')
print(f'La multiplicación de los dos números es: {numero1*numero2}')
print(f'La division de los dos números es: {numero1/numero2}')


